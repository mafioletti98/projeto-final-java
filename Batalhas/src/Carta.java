

public class Carta {

    private int ataque;
    private int defesa;
    

    public Carta(){

    }


    public Carta(int ataque, int defesa) {
        this.ataque = ataque;
        this.defesa = defesa;
    }


    public int getAtaque() {
        return ataque;
    }


    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }


    public int getDefesa() {
        return defesa;
    }


    public void setDefesa(int defesa) {
        this.defesa = defesa;
    }

    

}



